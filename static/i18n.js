    function format(str, vars) {
        if (!vars) return str;
        return str.replace(/\{\{(\w+)\}\}/g, (_, key) => {
            const value = vars[key];
            return value === undefined || value === null ? '' : String(value);
        });
    }

    const api = {
        _lang: 'en',
        init(lang) {
            this._lang = STRINGS[lang] ? lang : 'en';
            return this._lang;
        },
        t(key, vars) {
            const table = STRINGS[this._lang] || STRINGS.en;
            const fallback = STRINGS.en[key] ?? key;
            const raw = table[key] ?? fallback;
            return format(raw, vars);
        },
        apply(root = document) {
            root.querySelectorAll('[data-i18n]').forEach(el => {
                const key = el.getAttribute('data-i18n');
                const attr = el.getAttribute('data-i18n-attr');
                const html = el.hasAttribute('data-i18n-html');
                const val = this.t(key);
                if (attr) el.setAttribute(attr, val);
                else if (html) el.innerHTML = val;
                else el.textContent = val;
            });
        },
        has(key) {
            return key in (STRINGS[this._lang] || {}) || key in STRINGS.en;
        },
    };

    window._I18N_STRINGS = STRINGS;
    window._i18n = api;
